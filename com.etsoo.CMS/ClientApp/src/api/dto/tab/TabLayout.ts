/**
 * Tab layout
 */
export enum TabLayout {
  /**
   * Display one article
   */
  Article,

  /**
   * No display
   */
  None,

  /**
   * Title list
   */
  TitleList,

  /**
   * Title & description list
   */
  TitleDescriptionList,

  /**
   * Logo & title list
   */
  LogoList,

  /**
   * Title, logo & description list
   */
  FullList,

  /**
   * Title, logo & content list
   * 标题、图片和内容列表
   */
  ContentList,

  /**
   * Template data
   */
  Template
}
